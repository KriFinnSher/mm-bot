package main

func main() {
	app := &application{}
	app.init()

	message := `
Привет! Я бот для управления голосованиями в Mattermost. Вот что я могу:

**Команды:**

1. **Создать голосование:**
   - Формат: @vote-bot голосование "Вопрос" "Вариант1" "Вариант2" ...
   - Пример: @vote-bot голосование "Какой язык программирования ты предпочитаешь?" "Go" "Python" "Java" "Rust"

2. **Голосовать:**
   - Формат: @vote-bot голосовать "ID" "номер_варианта"
   - Пример: @vote-bot голосовать "poll-1632345678" "1"

3. **Показать результаты голосования:**
   - Формат: @vote-bot результаты "ID"
   - Пример: @vote-bot результаты "poll-1632345678"

4. **Завершить голосование:**
   - Формат: @vote-bot завершить "ID"
   - Пример: @vote-bot завершить "poll-1632345678"

5. **Удалить голосование:**
   - Формат: @vote-bot удалить "ID"
   - Пример: @vote-bot удалить "poll-1632345678"

**Примечания:**
- Все команды должны быть отправлены в канал, где я присутствую.
- Для создания голосования необходимо указать хотя бы два варианта ответа.
- Вы можете голосовать только за один вариант.
- Голосование можно завершить или удалить только создателю голосования.

Если у вас возникнут вопросы, просто напишите мне!
`
	app.sendMsgToChannel(message, "")

	app.startWebSocket()
}
